                <p>Устройство дренажного насоса состоит из двигателя и насосного узла, которые помещены в герметичный корпус. Изготавливается он в основном из нержавеющей стали или достаточно прочного пластика. Рабочее колесо двигателя снабжено лопастями и закреплено на валу ротора.
                </p>
                <p>Насосный узел всасывает воду через специальные отверстия, от диаметра которых зависит максимальный размер твердых частиц допустимых для содержания в откачиваемой жидкости. Большинство выпускаемых насосов снабжаются поплавковым выключателем.
                </p>
                <p>Прежде чем определиться какой дренажный насос выбрать, нужно выяснить преимущественные условия использования. Это необходимо для того, чтобы определится с комплектацией механизма.
                </p>
                <p>Основные характеристики, на которые следует обращать внимание при выборе дренажного насоса таковы: </p>
                <p>• нахождение всасывающего отверстия; Для профессиональных агрегатов эта особенность неважна. Они могут располагаться в любом положении. Для бытовых насосов лучше, если отверстие расположено снизу. Это позволит максимально откачивать воду со дна резервуара. </p>
